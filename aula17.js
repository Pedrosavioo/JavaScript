//=== WHILE ========================================================================================================================================
  let n = 0

  while (n<10) { // Enquanto n for menor que 10
    console.log(n); // Saída será o valor de n
    n++ // atribuindo +1 no valor de n
  }

  console.log('fim'); // fim do programa
//===========================================================================================================================================